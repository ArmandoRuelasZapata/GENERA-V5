/// Spacing and Size Constants
/// Provides consistent spacing values across the app
/// Following Material 3 and Apple aesthetics
class AppSpacing {
  // ============================================
  // PADDING VALUES
  // ============================================

  /// Standard screen padding
  static const double screenPadding = 16.0;

  /// Standard horizontal inset for edge-to-edge lists/carousels
  static const double carouselHorizontalInset = 16.0;

  /// Card internal padding
  static const double cardPadding = 16.0;

  /// Small padding for compact elements
  static const double smallPadding = 8.0;

  /// Large padding for spacious layouts
  static const double largePadding = 24.0;

  /// Extra large padding
  static const double xlPadding = 32.0;

  // ============================================
  // SPACING BETWEEN ELEMENTS
  // ============================================

  /// Tiny gap (between related elements)
  static const double tinyGap = 4.0;

  /// Small gap
  static const double smallGap = 8.0;

  /// Standard gap between elements
  static const double gap = 12.0;

  /// Medium gap
  static const double mediumGap = 16.0;

  /// Large gap between sections
  static const double largeGap = 24.0;

  /// Vertical separation between major sections
  static const double sectionSpacing = 24.0;

  /// Gap between section title and its content
  static const double sectionTitleGap = 12.0;

  /// Grid gaps for card grids
  static const double gridGap = 12.0;

  /// Width ratio for horizontal carousel cards
  static const double carouselCardViewport = 0.88;

  // ============================================
  // BORDER RADIUS VALUES
  // ============================================

  /// Standard card radius
  static const double cardRadius = 16.0;

  /// Button and tile radius
  static const double buttonRadius = 14.0;

  /// Small radius for compact elements
  static const double smallRadius = 8.0;

  /// Large radius for prominent elements
  static const double largeRadius = 20.0;

  // ============================================
  // ELEVATION VALUES
  // ============================================

  /// Minimal elevation for subtle depth
  static const double minimalElevation = 1.0;

  /// Low elevation for cards
  static const double lowElevation = 2.0;

  /// Medium elevation for floating elements
  static const double mediumElevation = 4.0;

  /// High elevation for modals and dialogs
  static const double highElevation = 8.0;

  // ============================================
  // ICON SIZES
  // ============================================

  static const double smallIcon = 18.0;
  static const double mediumIcon = 24.0;
  static const double largeIcon = 32.0;
  static const double xlargeIcon = 48.0;

  // ============================================
  // COMPONENT HEIGHTS
  // ============================================

  static const double buttonHeight = 48.0;
  static const double inputHeight = 56.0;
  static const double appBarHeight = 56.0;
  static const double bottomNavHeight = 72.0;
}
